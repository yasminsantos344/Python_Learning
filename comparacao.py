"""

Operadores de Comparação

Operador da Igualdade : ==
Operador de Atribuição : =
Operador da Diferença : !=
Operador maior que : >
Operador menor que : <
Operador maior que ou igual a : >=
Operador menor que ou igual a : <=


"""

x = 3
y = 5
z = 3.0
w = 8

print(x == y)  # 3 é igual a 5 #
print(x == z)  # 3 é igual a 3.0 #
print(x != z)  # 3 é diferente de 3.0 #
print(w > y)  # 8 é maior que 5 #
print(z < y)  # 3.0 é menor que 5 #
print(w >= y)  # 8 é maior ou igual a 5 #
print(x <= y)  # 3 é menor ou igual a 5 #